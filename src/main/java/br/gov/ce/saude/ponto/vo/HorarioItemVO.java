package br.gov.ce.saude.ponto.vo;

import java.io.Serializable;
import java.math.BigInteger;
import java.util.Date;
import org.codehaus.jackson.map.annotate.JsonSerialize;

/**
 *
 * @author esmayktillesse
 */
@JsonSerialize(include = JsonSerialize.Inclusion.NON_NULL)
public class HorarioItemVO implements Serializable{
    
    
    private Date entrada1;
    private Date saida1;
    private Date entrada2;
    private Date saida2;
    
    private String ent1;
    private String sai1;
    private String ent2;
    private String sai2;
    
    private BigInteger id;
    private BigInteger horario_fk;
    private Short dia_semana;
    private Boolean vira_dia;
    private Integer cod_horario;

    public HorarioItemVO() {
    }

    public HorarioItemVO(Date entrada1, Date saida1, Date entrada2, Date saida2, String ent1, String sai1, String ent2, String sai2, BigInteger id, BigInteger horario_fk, Short dia_semana, Boolean vira_dia, Integer cod_horario ) {
        this.entrada1 = entrada1;
        this.saida1 = saida1;
        this.entrada2 = entrada2;
        this.saida2 = saida2;
        this.ent1 = ent1;
        this.sai1 = sai1;
        this.ent2 = ent2;
        this.sai2 = sai2;
        this.id = id;
        this.horario_fk = horario_fk;
        this.dia_semana = dia_semana;
        this.vira_dia = vira_dia;
        this.cod_horario = cod_horario;
    }

    public Date getEntrada1() {
        return entrada1;
    }

    public void setEntrada1(Date entrada1) {
        this.entrada1 = entrada1;
    }

    public Date getSaida1() {
        return saida1;
    }

    public void setSaida1(Date saida1) {
        this.saida1 = saida1;
    }

    public Date getEntrada2() {
        return entrada2;
    }

    public void setEntrada2(Date entrada2) {
        this.entrada2 = entrada2;
    }

    public Date getSaida2() {
        return saida2;
    }

    public void setSaida2(Date saida2) {
        this.saida2 = saida2;
    }

    public String getEnt1() {
        return ent1;
    }

    public void setEnt1(String ent1) {
        this.ent1 = ent1;
    }

    public String getSai1() {
        return sai1;
    }

    public void setSai1(String sai1) {
        this.sai1 = sai1;
    }

    public String getEnt2() {
        return ent2;
    }

    public void setEnt2(String ent2) {
        this.ent2 = ent2;
    }

    public String getSai2() {
        return sai2;
    }

    public void setSai2(String sai2) {
        this.sai2 = sai2;
    }

    public BigInteger getId() {
        return id;
    }

    public void setId(BigInteger id) {
        this.id = id;
    }

    public BigInteger getHorario_fk() {
        return horario_fk;
    }

    public void setHorario_fk(BigInteger horario_fk) {
        this.horario_fk = horario_fk;
    }

    public Short getDia_semana() {
        return dia_semana;
    }

    public void setDia_semana(Short dia_semana) {
        this.dia_semana = dia_semana;
    }

    public Boolean getVira_dia() {
        return vira_dia;
    }

    public void setVira_dia(Boolean vira_dia) {
        this.vira_dia = vira_dia;
    }

    public Integer getCod_horario() {
        return cod_horario;
    }

    public void setCod_horario(Integer cod_horario) {
        this.cod_horario = cod_horario;
    }
    
    
    
}
